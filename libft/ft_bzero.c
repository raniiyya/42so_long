#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*d;

	d = (unsigned char *)s;
	i = 0;
	while (n > 0)
	{
		d[i] = 0;
		i++;
		n--;
	}
}
